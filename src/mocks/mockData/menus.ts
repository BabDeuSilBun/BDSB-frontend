const pageSize = 10;

export const paginatedMenus: { [storeId: number]: MenusResponse[] } =
  menuData.reduce(
    (acc, { storeId, menus }) => {
      const totalPages = Math.ceil(menus.length / pageSize);

      const paginatedItems = Array.from({ length: totalPages }, (_, index) => {
        const start = index * pageSize;
        const end = start + pageSize;

        const itemsForCurrentPage = menus.slice(start, end);

        return {
          storeId,
          content: itemsForCurrentPage,
          pageable: {
            offset: start,
            pageNumber: index,
            pageSize,
            sort: {
              empty: true,
              unsorted: true,
              sorted: false,
            },
            paged: true,
            unpaged: false,
          },
          last: index === totalPages - 1,
          totalPages,
          size: pageSize,
          first: index === 0,
          number: index,
          numberOfElements: itemsForCurrentPage.length,
          sort: {
            empty: true,
            unsorted: true,
            sorted: false,
          },
          totalElements: menus.length,
          empty: itemsForCurrentPage.length === 0,
        } as MenusResponse;
      });

      acc[storeId] = paginatedItems;
      return acc;
    },
    {} as { [storeId: number]: MenusResponse[] },
  );
